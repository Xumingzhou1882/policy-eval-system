# ═══════════════════════════════════════════════════════════════════════
# Name variant generation
# ═══════════════════════════════════════════════════════════════════════

def generate_variants(canonical_name: str, aliases: list[str]) -> list[str]:
    """Generate all common name variants for a city."""
    variants = set()
    variants.add(canonical_name)

    # Strip autonomous prefecture suffixes first (longest patterns first)
    ethnic_suffixes = [
        "土家族苗族自治州", "苗族侗族自治州", "布依族苗族自治州",
        "傣族景颇族自治州", "蒙古族藏族自治州", "藏族羌族自治州",
        "哈尼族彝族自治州", "傈僳族自治州", "柯尔克孜自治州",
        "哈萨克自治州", "蒙古自治州", "彝族自治州", "藏族自治州",
        "回族自治州", "壮族自治州", "朝鲜族自治州", "白族自治州",
        "自治州", "自治县",
    ]
    base = canonical_name
    for sfx in ethnic_suffixes:
        if base.endswith(sfx):
            base = base[:-len(sfx)]
            variants.add(base)
            break

    # Strip general region suffixes
    suffixes = ["市", "省", "特别行政区", "自治区", "地区", "州", "盟",
               "开发区", "新区"]
    base2 = base
    for sfx in suffixes:
        if base2.endswith(sfx):
            base2 = base2[:-len(sfx)]
            variants.add(base2)
            break

    # Add Chinese aliases (with suffix stripping)
    for alias in aliases:
        variants.add(alias)
        for sfx in ethnic_suffixes + suffixes:
            if alias.endswith(sfx):
                variants.add(alias[:-len(sfx)])

    return sorted(variants)


# ═══════════════════════════════════════════════════════════════════════
# Main generator
# ═══════════════════════════════════════════════════════════════════════

def generate_entity_map() -> dict[str, str]:
    """Build complete entity_map from GB/T 2260 codes."""
    entity_map: dict[str, str] = {}

    all_entries = PREFECTURE_CODES + PROVINCE_CODES

    for code, canonical, aliases in all_entries:
        variants = generate_variants(canonical, aliases)
        for v in variants:
            v = v.strip()
            if not v:
                continue
            if v in entity_map and entity_map[v] != code:
                # Conflict: keep the shorter code (usually prefecture)
                existing = entity_map[v]
                if len(code) < len(existing):
                    entity_map[v] = code
            else:
                entity_map[v] = code

    return entity_map


# ═══════════════════════════════════════════════════════════════════════
# CLI
# ═══════════════════════════════════════════════════════════════════════

def main():
    parser = argparse.ArgumentParser(
        description="Generate entity_map.json from GB/T 2260 codes")
    parser.add_argument("--output", default=None,
                        help="Output path (default: data/auto/entity_map.json)")
    args = parser.parse_args()

    entity_map = generate_entity_map()

    output = Path(args.output) if args.output else (
        Path(__file__).resolve().parent.parent / "data" / "auto" / "entity_map.json")
    output.parent.mkdir(parents=True, exist_ok=True)

    with open(output, "w", encoding="utf-8") as f:
        json.dump(entity_map, f, ensure_ascii=False, indent=2)

    print(f"Generated entity_map.json with {len(entity_map)} entries")
    print(f"Saved to {output}")

    # Quick stats
    codes = set(entity_map.values())
    print(f"Unique codes: {len(codes)}")
    print(f"Average variants per entity: {len(entity_map) / len(codes):.1f}")


if __name__ == "__main__":
    main()
